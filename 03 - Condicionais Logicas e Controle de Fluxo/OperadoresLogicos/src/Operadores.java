public class Operadores {
    public static void main(String[] args) {

        /*
        conjunção -> E -> &&
        disjunção -> OU -> ||
        Disjunção exclusiva -> Ou Ou -> ^
        Negação - não/inversão -> !

         */
        boolean b1 = true;
        boolean b2 = false;
        boolean b3 = true;

        System.out.println(b1 ^ b3);
    }
}